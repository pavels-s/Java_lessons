package main.java.JavaGuru_2020_Java1.lesson_10.level_6;

class Book {

    private String author, title;

    Book (String title, String author) {
        this.author = author;
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public String getTitle() {
        return title;
    }
}
